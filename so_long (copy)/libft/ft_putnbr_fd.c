#include "libft.h"

int	ft_putnbr(long nb)

{
	int	i;

	i = 0;
	if (nb < 0)
	{
		i += write(1, "-", 1);
		nb *= -1;
	}
	if (nb >= 10)
	{
		i += ft_putnbr(nb / 10);
	}
	i += ft_putchar((nb % 10) + '0');
	return (i);
}
